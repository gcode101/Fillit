#include "fillit.h"

int		minos_count(char *str)
{
	int		i;
	int		ct;

	ct = 0;
	i = 0;
	while (str[i])
	{
		if (str[i] == '\n' && str[i + 1] == '\n')
			ct++;
		i++;
	}
	return (ct + 1);
}
